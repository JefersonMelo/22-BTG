# Tipos e entrada de dados

# Strings
bv = 'Seja bem vindo!'
nome = input('Digite seu nome: ')

# Interpolação de Strings
print(f'{nome}, {bv}')
# ou
print('{}, {}'.format(nome, bv))

# Numbers
nInt = int(input('Tipo Int: '))
print(nInt)
nFloat = float(input('Tipo Float: '))
print(nFloat)

# Boolean
vBool = bool(input('Valor Boolean: '))
print(vBool)


